func CreateBigquery_datasets_undeleteTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_projects_projectId_datasets_datasetId:undelete",
		mcp.WithDescription("Undeletes a dataset which is within time travel window based on datasetId. If a time is specified, the dataset version deleted at that time is undeleted, else the last live version is undeleted."),
		mcp.WithString("projectId", mcp.Required(), mcp.Description("Required. Project ID of the dataset to be undeleted")),
		mcp.WithString("datasetId", mcp.Required(), mcp.Description("Required. Dataset ID of dataset being deleted")),
		mcp.WithString("deletionTime", mcp.Description("Input parameter: Optional. The exact time when the dataset was deleted. If not specified, it will undelete the most recently deleted version.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Bigquery_datasets_undeleteHandler(cfg),
	}
}
